<p>Переключатель по углу поворота в фиксированные положения подразделяется на четыре исполнения:</br>
а) 1 - 30°;</br> 
б) 6 - 60°;</br> 
в) 8 - 45°;</br> 
г) 4 - 90°.</br>
Рабочее положение в пространстве – любое.</br>
Максимальное количество положений переключения при угле 30° - 12.</br>
Максимальное количество галет – 12 (контактов – 24).</br>
Диапазон рабочих температур от минус 30°С до плюс 55°С.</br>
Механическая износостойкость не менее 1000000  переключений.</br>
Климатическое исполнение "0", категория размещения 4 по ГОСТ 15150  и ГОСТ 15543.1.</br>
