<p><strong>Вопрос</strong>: Какие виды переключателей вы производите? Какие типы переключателей вы производите? </p>
<p><strong>Ответ</strong>:</p>
<p>Виды кулачковых переключателей:</p>
<p> </p>
<p>            S…J - С ручкой (без лицевой панели),</p>
<p> </p>
<p>            S…JG - С ручкой и уплотнением по оси ручки до IP65,</p>
<p> </p>
<p>            S…JD - С ручкой и лицевой панелью,</p>
<p> </p>
<p>            S…JU - С ручкой и возможностью применения навесных замков,</p>
<p> </p>
<p>            S…JV - С одним или двумя возвратными положениями (только S10,16,25J),</p>
<p> </p>
<p>            S…JK - Быстро монтируемый (Ø22мм) с управлением ключом (только S10,16,25J),</p>
<p> </p>
<p>            S…JО - Заднее крепление переключателя (на монтажную панель),</p>
<p> </p>
<p>            S…JLD - С креплением на DIN-рейку 35х7,5 с лицевой панелью,</p>
<p> </p>
<p>            S…JB - Заднее крепление переключателя – с ручкой на двери,</p>
<p> </p>
<p>            S…JBD - Заднее крепление переключателя – с ручкой и лицевой панелью на двери,</p>
<p> </p>
<p>            S…JBU - Заднее крепление переключателя – с замыкающейся ручкой на двери,</p>
<p> </p>
<p>            S…JP - В герметичном корпусе из пластика без лицевой панели (IP 54),</p>
<p> </p>
<p>            S…JPD - В герметичном корпусе из пластика с лицевой панелью (IP 54),</p>
<p> </p>
<p>            S…JPU - В герметичном корпусе из пластика с замыкающейся ручкой (IP 54).</p>
