<<h1><span>Часто задаваемые вопросы по кулачковым переключателям</span></h1>
<p><strong>Вопрос</strong>: Зависит ли цена на переключатели от количества?</p>
<p><strong>Ответ</strong>: Да.</p>
<p> </p>
<p><strong>Вопрос</strong>: Можно ли купить переключатели без предоплаты?</p>
<p><strong>Ответ</strong>: Нет.</p>
<p> </p>
<p><strong>Вопрос</strong>: Возможно ли изготовление переключателя на 13 и более положений ручки?</p>
<p><strong>Ответ</strong>: Нет.</p>
<p> </p>
<p><strong>Вопрос</strong>: Предназначены ли ваши переключатели для коммутации постоянного тока?</p>
<p><strong>Ответ</strong>: Да.</p>
<p> </p>
<p><strong>Вопрос</strong>: можно ли у вас купить переключатель на din-рейку?</p>
<p><strong>Ответ</strong>: да. В обозначении переключателя с креплением на дин-рейку должна быть буква L. Например, <a title="переключатель S25JLD1103A8" href="http://khrl.com.ua/switcher/S25JLD1103A8">S25JLD1103A8</a>.</p>
<p> </p>
<p><strong>Вопрос</strong>: Можно ли у вас купить переключатель с ключом?</p>
<p><strong>Ответ</strong>: Да. В обозначении переключателя с ключом должна быть буква K. Например, <a title="переключатель S25JK1108A6" href="http://khrl.com.ua/switcher/S25JK1108A6">S25JK1108A6</a>.</p>
<p> </p>
<p><strong>Вопрос</strong>: Можно ли у вас купить переключатель без фиксации?</p>
<p><strong>Ответ</strong>: Да. В обозначении переключателя без фиксации должна быть буква V. Например, <a title="переключатель S25JVD2202C8" href="http://khrl.com.ua/switcher/S25JVD2202C8">S25JVD2202C8</a>.</p>
<p> </p>
<p><strong>Вопрос</strong>: Где можно просмотреть каталог переключателей, которые вы производите?</p>
<p><strong>Ответ</strong>: Список производимых переключателей можно просмотреть на странице </p>
<p><a href="switcher/list.php"> </a><a href="switcher/list.php">Перечень переключателей</a>.</p>
<p> </p>
<p><strong>Вопрос</strong>: Можно ли у вас купить переключатель в корпусе?</p>
<p><strong>Ответ</strong>: Да. В обозначении переключателя в корпусе быть буква P. Например,  <a title="переключатель S25JPD1102A4" href="http://khrl.com.ua/switcher/S25JPD1102A4">S25JPD1102A4</a>.</p>
<p> </p>
<p><strong>Вопрос</strong>: Где можно просмотреть диаграмму переключателя?</p>
<p><strong>Ответ</strong>: Диаграмму замыканий контактов переключателя можно просмотреть на странице <a href="scheme/">Схемы кулачковых переключателей</a>.</p>
<p> </p>
<p><strong>Вопрос</strong>: Имеется ли на ваши переключатели ГОСТ?</p>
<p><strong>Ответ</strong>: Нет. На наши переключатели имеются только <a href="certificates">Технические условия</a>.</p>
<p> </p>
<p><strong>Вопрос</strong>: Имеется ли на ваши переключатели сертификат качесива?</p>
<p><strong>Ответ</strong>: Да. Просмотреть его можно на странице <a href="certificates">Сертификаты на кулачковые переключатели</a>.</p>
<p> </p>
<p><strong>Вопрос</strong>: Можно ли у вас купить ступенчатый переключатель? </p>
<p><strong>Ответ</strong>: Да. Наше предприятия производит большой ассортимент ступенчатых переключателей. Схемы и диаграммы замыканий на ступенчатые переключатели можно просмотреть на странице <a href="scheme/">Схемы кулачковых переключателей</a>.</p>
<p> </p>
<p><strong>Вопрос</strong>: Можно ли у вас купить переключатель с ip21?</p>
<p><strong>Ответ</strong>: Да. Все наши переключатели имеют степень защиты ip21.</p>
<p> </p>
<p><strong>Вопрос</strong>: Можно ли у вас купить переключатель с ip44?</p>
<p> </p>
<p><strong>Ответ</strong>: Нет. Наши переключатели имеют степени защиты ip21 и ip54.</p>
<p> </p>
<p> </p>
<p><strong>Вопрос</strong>: Можно ли у вас купить переключатель с ip55?</p>
<p><strong>Ответ</strong>: Нет. Максимальная степень защиты у наших переключателей ip54.</p>
<p> </p>
<p><strong>Вопрос</strong>: Производители ли вы ремонт переключателей?</p>
<p><strong>Ответ</strong>: Да. Мы производим гарантийный и послегарантийный ремонт переключателей, но только своего производства.</p>
<p> </p>
<p><strong>Вопрос</strong>: Где можно просмотреть электросхему переключателя?</p>
<p><strong>Ответ</strong>: Электросхему переключателя можно просмотреть на странице <a href="scheme/">Схемы кулачковых переключателей</a>.</p>
<p> </p>
<p><strong>Вопрос</strong>: Можно ли у вас купить силовой переключатель?</p>
<p><strong>Ответ</strong>: Да. Все наши переключатели предназначены для коммутации силовых цепей.</p>
