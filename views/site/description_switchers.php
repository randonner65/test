<h1>Описание кулачковых переключателей</h1>
<h2>Общие сведения</h2>
<p>Ряд кулачковых переключателей серии S10,16,25,32,63,100,160J новое поколение переключателей в электрическом ряду от 10 до 160А. Переключатели по габаритным размерам разделены на три группы. – 1-й размер – S10J, S16J, S25J – размер переключающего механизма 43х43х(13,5n)*мм – 2-й размер – S32J, S63 – размер переключающего механизма 66х66х(18,5n)*мм – 3-й размер – S100J, S160J – размер переключающего механизма 77х77х(21n)*мм *– размер в скобке – обозначает высоту одной галеты, n – количество галет. Электрические выводы переключателя выполнены под винтовой зажим с прижимной пластиной. Все зажимы и соединения в переключателе защищены от прямого соприкосновения обслуживающего персонала с токоведущими элементами. Степень защиты переключателей IP21 по ГОСТ 14254 или IP54 для исполнения G (уплотнение по оси ручки). Переключатель по способу крепления подразделяется на исполнения: – крепление на переднюю (лицевую) панель</p>
