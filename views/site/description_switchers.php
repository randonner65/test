<p>Переключатель по углу поворота в фиксированные положения подразделяется на четыре исполнения: а) 1 - 30°; б) 6 - 60°; в) 8 - 45°; г) 4 - 90°. Рабочее положение в пространстве – любое. Максимальное количество положений переключения при угле 30° - 12. Максимальное количество галет – 12 (контактов – 24). Диапазон рабочих температур от минус 40°С до плюс 60°С. Механическая износостойкость не менее 1000000 переключений. Климатическое исполнение "0", категория размещения 4 по ГОСТ 15150 и ГОСТ 15543.1.</p>
